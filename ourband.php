				<p>MSAPD was formed in 1996 from the merger of two well-known bagpipe bands,
				Northern Border Caledonia and Acadian Pipes & Drums.  Under the leadership
				of Pipe Major Scott Heney and Drum Major Anthea O'Neal, the band includes musicians
				with experience which ranges from 45 years to less than 6 months.  Members and
				apprentices from all over Central, Midcoast and Downeast Maine travel to
				Ellsworth for weekly rehearsals.  Following the long standing traditions 
				established by its two predecessors, MSAPD entertains audiences throughout Maine
				in parades and concerts, competes in Highland Games in New England and Canada,
				and provides educational presentations to Maine's school children.</p>
				<p>The band wears the Maine State Tartan. Designed in 1964, it is
				believed to be the oldest district tartan in the United States. Its
				colors include light blue, representing the sky, dark blue for the
				waters, dark green for the forests, and a thin red line for the
				bloodline of the people.</p>
